                        <h3>Mathematics</h3>
                        <p>Dr. Montessori believed in introducing children to the big picture before moving to the smaller portions. You will commonly see children introduced to the decimal system and working on 4 digit addition rather than single digit.
                            These concepts are taught by teachers and it is not until the child can display a concrete understanding that they are introduced to different methods of thinking. At this point, the child is free to continually explore math
                            materials without fear or anxiety.</p>
                        <h3>Cultural</h3>
                        <p>A young child's mind is an absorbent one. Dr. Montessori believes the absorbent mind allows children to take in all there is to offer. By introducing your child to these topics and subjects at a young age they are able to recall
                            it later on in their academic years. You will see children listening to different styles of music or looking at different well known artists, diversity is encouraged in this setting. The cultural area allows the child to gain
                            a better appreciation for each other.</p>
