                        <h3>What Do We Mean "Practical Life"?</h3>
                        <p>Practical life is an umbrella term for everyday living, it serves to develop your child's sense of order, coordination, and independence. Your child will practice the basic's such as how to tie their shoes, how to clean up a spill,
                            or even how to water plants. As we mentioned, children at this age are curious and want to explore which means a child will happily attain this information through repetition. Your child will learn how to care for themselves,
                            others, and the classroom. </p>
                        <h3>Sensory Education</h3>
                        <p>It is important to allow children to develop their senses, which is why we focus a portion of this class to sensory. Dr. Montessori designed materials specifically for sensory to focus on visual discrimination and to help refine
                            and develop the five senses. Through this, your child will explore the classification, contrasting, and comparison of color, shape, smell, feel, temperature, weights, and textures.</p>
                        <h3>Language</h3>
                        <p>In this area you will find different exercises that expose the child to new vocabulary. The different aspects of language truly come to life without undue stress to the child. Peer interactions are encouraged as well as any other
                            opportunities for meaningful interactions. These interactions really blossom as the child gains self-confidence as well as a larger vocabulary. This is all very crucial to every child's development and should be a priority
                            as they grow and develop.</p>
